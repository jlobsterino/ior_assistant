def classify_intent_with_qwen(user_query: str) -> str:
    messages = [
        {"role": "system", "content": INTENT_CLASSIFIER_PROMPT},
        {"role": "user", "content": f"Запрос пользователя: \"{user_query}\"\nКатегория:"}
    ]
