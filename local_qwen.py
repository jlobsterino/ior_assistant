def answer_follow_up_with_qwen(user_query: str, descriptions: list, history: list = None) -> str:
    import logging
    import re
    logger = logging.getLogger(__name__)
    
    logger.info(f"[QWEN_DEBUG] Получено описаний для QWEN: {len(descriptions)}")
    for idx, desc in enumerate(descriptions[:3], start=1):
        logger.info(f"[QWEN_DEBUG] Текст №{idx} (первые 120 символов): {str(desc)[:120]}...")

    if not descriptions:
        return "К сожалению контекст пуст. Сначала выполните поиск или выгрузку по ИОР"

    formatted_docs = ""
    for idx, item in enumerate(descriptions, start=1):
        if isinstance(item, dict):
            doc_id = item.get("id", f"№{idx}")
            desc = item.get("text", "")
        else:
            doc_id = f"№{idx}"
            desc = str(item)
            
        clean_desc = desc.strip()
        clean_desc = re.sub(r'(\n\s*)+\s*\n', '\n', clean_desc)
        clean_desc = re.sub(r'\n{3,}', '\n\n', clean_desc)
        formatted_docs += f"--- ИОР ID: {doc_id} ---\n{clean_desc}\n\n"
        
    prompt = QWEN_FOLLOW_UP_TEMPLATE.format(
        user_query=user_query,
        ior_descriptions=formatted_docs
    )
    
    if history:
        messages = [
            {"role": "system", "content": "Ты полезный ИИ-помощник, который отлично анализирует тексты"}
        ]
        messages.extend(history)
        messages.append({"role": "user", "content": prompt})
    else:
        messages = [
            {"role": "system", "content": "Ты полезный ИИ-помощник, который отлично анализирует тексты"},
            {"role": "user", "content": prompt}
        ]
        
    response = ask_local_qwen(messages, max_tokens=1500)
    return response

def clean_json(raw_text: str) -> dict:
    text = raw_text.strip()
    
    if text.startswith("```"):
        text = re.sub(r"\n```$", "", text)
        text = text.strip()
        
    try:
        return json.loads(text)
    except json.JSONDecodeError as e:
        print("Json error")
        return {
            "answer_found": False,
            "summary": "Не удалось корректно обработать ответ системы. Повторите запрос или обратитесь в поддержку."
        }

def summarize_iors(topic: str, descriptions: list) -> dict:
    if not descriptions:
        return {"answer_found": False, "summary": "Список описаний ИОР пуст."}
        
    formatted_descriptions = ""
    for idx, desc in enumerate(descriptions, start=1):
        clean_desc = str(desc).strip()[:300]
        if clean_desc:
            formatted_descriptions += f"ИОР №{idx}:\n{desc.strip()}\n\n"
            
    if not formatted_descriptions:
        return {"answer_found": False, "summary": "Доступные содержания ИОР не содержат текстовой информации"}
        
    prompt = IOR_SUMMARIZE_TEMPLATE.replace("{user_query}", topic).replace("{ior_descriptions}", formatted_descriptions)
    
    messages = [
        {"role": "system", "content": "Ты полезный ИИ-ассистент, который всегда отвечает строго в формате JSON"},
        {"role": "user", "content": prompt}
    ]
    
    try:
        response_text = ask_local_qwen(messages, max_tokens=256)
        
        if not response_text:
            return {"answer_found": False, "summary": "Модель вернула пустой ответ"}
            
        result = clean_json(response_text)
        print(f"Ответ QWEN: {repr(result)}")
        return result
        
    except Exception as e:
        print(f"Ошибка суммаризации ИОР: {e}")
        return ""